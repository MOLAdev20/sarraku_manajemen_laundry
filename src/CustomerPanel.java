    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if( IDUpdate <= 0 ){
            JOptionPane.showMessageDialog(null, "Pilih dulu data yang mau diedit");
        }else{
            setVariableToField();
            String queryUpdate = "UPDATE tb_customer SET nama='"+nama+"', gender='"+gender+"', no_hp='"+hp+"', alamat='"+alamat+"' WHERE id="+IDUpdate;
            if(KoneksiDB.runModifiedDataQuery(queryUpdate)){
                JOptionPane.showMessageDialog(null, "Data berhasil diedit!");
            }else{
                JOptionPane.showMessageDialog(null, "Data gagal diedit!");
            }
            getDataCustomer();
            clearAllFields();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if( IDUpdate <= 0 ){
            JOptionPane.showMessageDialog(null, "Pilih dulu data yang mau dihapus");
        }else{
            
            int konfirmasi = JOptionPane.showConfirmDialog(
                null,
                "Hapus Pelanggan ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
            );

            if (konfirmasi == JOptionPane.YES_OPTION) {
                String queryDelete = "DELETE FROM tb_customer WHERE id="+IDUpdate;
                if(KoneksiDB.runModifiedDataQuery(queryDelete)){
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
                }else{
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus!");
                }
                getDataCustomer();
            }
            clearAllFields();
        }
    }//GEN-LAST:event_jButton5ActionPerformed


