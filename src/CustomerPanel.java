    private void setVariableToField(){
        nama = namaField.getText();
        hp = hpField.getText();
        alamat = alamatField.getText();
        
        if(maleRadio.isSelected()){
            gender = "Laki-laki";
        }else if(femaleRadio.isSelected()){
            gender = "Perempuan";
        }
    }
    
    private void getDataCustomer(){
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("ID");
        model.addColumn("No");
        model.addColumn("Nama");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Nomor Hp");
        model.addColumn("Alamat");
        
        List<Map<String, Object>> hasil = KoneksiDB.runSelectQuery("SELECT * FROM tb_customer");

        int no = 1;
        for (Map<String, Object> row : hasil) {
            model.addRow(new Object[] {
                row.get("id"),
                no++,
                row.get("nama"),
                row.get("gender"),
                row.get("no_hp"),
                row.get("alamat"),
            });
        }

        tableCustomer.setModel(model);
        tableCustomer.getColumnModel().getColumn(0).setMinWidth(0);
        tableCustomer.getColumnModel().getColumn(0).setMaxWidth(0);
        tableCustomer.getColumnModel().getColumn(0).setWidth(0);
    }
    
